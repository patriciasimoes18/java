public class Lacos {
    public static void main(String[] args) {
        //indice
        int indice = 0;

        //While
        while(indice<11){
            System.out.println(indice);
            indice++;
        }
        
        // separar
        System.out.println("______________________________");
        
        //Do While
        do{
            System.out.println(indice);
            indice--;

        }while(indice>=0);


    }
}
