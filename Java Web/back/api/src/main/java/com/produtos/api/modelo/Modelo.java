package com.produtos.api.modelo;

public class Modelo {
    //Atributo
    private String nome;
    private int idade;

    //get em set
    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return this.idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
